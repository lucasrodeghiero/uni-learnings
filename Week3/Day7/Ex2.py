import turtle

arrow = turtle.Turtle()
# Step 1
arrow.forward(50)
# Step 2
arrow.left(90)
# Step 3
arrow.forward(100)
# Step 4
arrow.right(90)
# Step 5
arrow.forward(25)
# Step 6
arrow.setheading(120)
# Step 7
arrow.forward(100)
# Step 8
arrow.setheading(240)
# Step 9
arrow.forward(100)
# Step 10 # 0 is initial position
arrow.setheading(0)
# Step 11
arrow.forward(25)
# Step 12
arrow.right(90)
# Step 13
arrow.forward(100)


turtle.done()
