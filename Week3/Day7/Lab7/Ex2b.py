import turtle

myTurtle = turtle.Turtle()

myTurtle.width(3)
myTurtle.penup()
myTurtle.goto(0,150)
myTurtle.color('red')
myTurtle.pendown()
myTurtle.forward(100)
myTurtle.penup()
myTurtle.goto(30,-70)
myTurtle.pendown()
myTurtle.color('purple')
myTurtle.setheading(90)
myTurtle.forward(65)
turtle.done()

