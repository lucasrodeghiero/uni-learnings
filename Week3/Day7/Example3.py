import turtle

# create new turtle called "slow"
slow = turtle.Turtle()
# move 50 pixels forward
slow.forward(50)
# turn right
slow.right(90)
#move 50 pixels forward
slow.forward(50)
# turn left
slow.left(90)
# move 50 pixels forward
slow.forward(50)
# turn direction south west (degrees/angle)
slow.setheading(210)
# move 150 pixels forward
slow.forward(150)
# complete
turtle.done()
